class Cube {
    constructor(gl, program) {
        this.gl = gl;
        this.program = program;
        this.transform = new Transform();
        this.mesh = null;
        this.color = [1, 1, 1, 1];  // ← ADICIONE
        this.useTexture = false;    // ← ADICIONE
        this.texture = null;        // ← ADICIONE
        this.create();
    }
    
    create() {
        var vertices = new Float32Array([
            // Face frontal (z=0.5) - normal (0,0,1)
            -0.5,  0.5,  0.5,   0, 0, 1,   0.0, 0.0,
            -0.5, -0.5,  0.5,   0, 0, 1,   0.0, 1.0,
             0.5, -0.5,  0.5,   0, 0, 1,   1.0, 1.0,
            -0.5,  0.5,  0.5,   0, 0, 1,   0.0, 0.0,
             0.5, -0.5,  0.5,   0, 0, 1,   1.0, 1.0,
             0.5,  0.5,  0.5,   0, 0, 1,   1.0, 0.0,
            
            // Face traseira (z=-0.5) - normal (0,0,-1)
            -0.5,  0.5, -0.5,   0, 0, -1,   1.0, 0.0,
            -0.5, -0.5, -0.5,   0, 0, -1,   1.0, 1.0,
             0.5, -0.5, -0.5,   0, 0, -1,   0.0, 1.0,
            -0.5,  0.5, -0.5,   0, 0, -1,   1.0, 0.0,
             0.5, -0.5, -0.5,   0, 0, -1,   0.0, 1.0,
             0.5,  0.5, -0.5,   0, 0, -1,   0.0, 0.0,
            
            // Face esquerda (x=-0.5) - normal (-1,0,0)
            -0.5,  0.5, -0.5,   -1, 0, 0,   0.0, 0.0,
            -0.5, -0.5, -0.5,   -1, 0, 0,   0.0, 1.0,
            -0.5, -0.5,  0.5,   -1, 0, 0,   1.0, 1.0,
            -0.5,  0.5, -0.5,   -1, 0, 0,   0.0, 0.0,
            -0.5, -0.5,  0.5,   -1, 0, 0,   1.0, 1.0,
            -0.5,  0.5,  0.5,   -1, 0, 0,   1.0, 0.0,
            
            // Face direita (x=0.5) - normal (1,0,0)
             0.5,  0.5,  0.5,   1, 0, 0,   0.0, 0.0,
             0.5, -0.5,  0.5,   1, 0, 0,   0.0, 1.0,
             0.5, -0.5, -0.5,   1, 0, 0,   1.0, 1.0,
             0.5,  0.5,  0.5,   1, 0, 0,   0.0, 0.0,
             0.5, -0.5, -0.5,   1, 0, 0,   1.0, 1.0,
             0.5,  0.5, -0.5,   1, 0, 0,   1.0, 0.0,
            
            // Face superior (y=0.5) - normal (0,1,0)
            -0.5,  0.5, -0.5,   0, 1, 0,   0.0, 0.0,
            -0.5,  0.5,  0.5,   0, 1, 0,   0.0, 1.0,
             0.5,  0.5,  0.5,   0, 1, 0,   1.0, 1.0,
            -0.5,  0.5, -0.5,   0, 1, 0,   0.0, 0.0,
             0.5,  0.5,  0.5,   0, 1, 0,   1.0, 1.0,
             0.5,  0.5, -0.5,   0, 1, 0,   1.0, 0.0,
            
            // Face inferior (y=-0.5) - normal (0,-1,0)
            -0.5, -0.5,  0.5,   0, -1, 0,   0.0, 0.0,
            -0.5, -0.5, -0.5,   0, -1, 0,   0.0, 1.0,
             0.5, -0.5, -0.5,   0, -1, 0,   1.0, 1.0,
            -0.5, -0.5,  0.5,   0, -1, 0,   0.0, 0.0,
             0.5, -0.5, -0.5,   0, -1, 0,   1.0, 1.0,
             0.5, -0.5,  0.5,   0, -1, 0,   1.0, 0.0
        ]);
        
        this.mesh = new Mesh(this.gl, vertices, 3, 3, 2, this.program);
    }
    
    // ==========================================
    // MÉTODOS QUE FALTAVAM
    // ==========================================
    setColor(r, g, b, a = 1) {
        this.color = [r, g, b, a];
        this.useTexture = false;
    }
    
    setTexture(texture) {
        this.texture = texture;
        this.useTexture = true;
    }
    
    draw() {
        if (this.mesh) {
            this.mesh.draw();
        }
    }
    // ==========================================
    
    getMesh() {
        return this.mesh;
    }
    
    drawRange(start, count) {
        this.mesh.drawRange(start, count);
    }
}